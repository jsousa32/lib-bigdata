package io.github.jsousa32.lib_bigdata.people.entities.government_debtors;

public class Debt {

    private String source;

    private String debtOrigin;

    private double consolidatedValue;

    private String debtorType;

    private String responsibleUnity;

    private String responsibleUnityUF;

    private String registrationNumber;

    private String registrationSituationType;

    private String registrationSituation;

    private boolean filedIndicator;

    private String registrationDate;

    private String creationDate;

    private String lastUpdateDate;

    public String getSource() {
        return source;
    }

    public String getDebtOrigin() {
        return debtOrigin;
    }

    public double getConsolidatedValue() {
        return consolidatedValue;
    }

    public String getDebtorType() {
        return debtorType;
    }

    public String getResponsibleUnity() {
        return responsibleUnity;
    }

    public String getResponsibleUnityUF() {
        return responsibleUnityUF;
    }

    public String getRegistrationNumber() {
        return registrationNumber;
    }

    public String getRegistrationSituationType() {
        return registrationSituationType;
    }

    public String getRegistrationSituation() {
        return registrationSituation;
    }

    public boolean isFiledIndicator() {
        return filedIndicator;
    }

    public String getRegistrationDate() {
        return registrationDate;
    }

    public String getCreationDate() {
        return creationDate;
    }

    public String getLastUpdateDate() {
        return lastUpdateDate;
    }
}
