package io.github.jsousa32.lib_bigdata.people.entities.occupation_data;

public class AdditionalDetails {

    private String situation;

    private boolean isDead;

    private String companyAdmissionDate;

    private String jobFunction;

    private String jobRole;

    private String servantType;

    private String workLoad;

    private String workingPlace;

    private String organType;

    private String headBranch;

    public String getSituation() {
        return situation;
    }

    public boolean isDead() {
        return isDead;
    }

    public String getCompanyAdmissionDate() {
        return companyAdmissionDate;
    }

    public String getJobFunction() {
        return jobFunction;
    }

    public String getJobRole() {
        return jobRole;
    }

    public String getServantType() {
        return servantType;
    }

    public String getWorkLoad() {
        return workLoad;
    }

    public String getWorkingPlace() {
        return workingPlace;
    }

    public String getOrganType() {
        return organType;
    }

    public String getHeadBranch() {
        return headBranch;
    }
}
