package io.github.jsousa32.lib_bigdata.people.entities.university_student_data;

public class ScholarshipHistory {

    private String level;

    private String institution;

    private String specializationArea;

    private String courseType;

    private String courseShift;

    private String scholarshipType;

    private String startYear;

    private String endYear;

    private String academicTrainingType;

    private String institutionCampus;

    private String educationalLevel;

    public String getLevel() {
        return level;
    }

    public String getInstitution() {
        return institution;
    }

    public String getSpecializationArea() {
        return specializationArea;
    }

    public String getCourseType() {
        return courseType;
    }

    public String getCourseShift() {
        return courseShift;
    }

    public String getScholarshipType() {
        return scholarshipType;
    }

    public String getStartYear() {
        return startYear;
    }

    public String getEndYear() {
        return endYear;
    }

    public String getAcademicTrainingType() {
        return academicTrainingType;
    }

    public String getInstitutionCampus() {
        return institutionCampus;
    }

    public String getEducationalLevel() {
        return educationalLevel;
    }
}
